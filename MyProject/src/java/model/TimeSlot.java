package model;

/**
 *
 * @author Trường Xuân
 */
public class TimeSlot {
    private int tid;
    private String tfrom;
    private String tto;

    public TimeSlot() {
    }

    public int getTid() {
        return tid;
    }

    public void setTid(int tid) {
        this.tid = tid;
    }

    public String getTfrom() {
        return tfrom;
    }

    public void setTfrom(String tfrom) {
        this.tfrom = tfrom;
    }

    public String getTto() {
        return tto;
    }

    public void setTto(String tto) {
        this.tto = tto;
    }
    
}
